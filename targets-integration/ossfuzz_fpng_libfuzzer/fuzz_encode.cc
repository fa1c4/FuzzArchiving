#include <stdint.h>
#include <stdlib.h>
#include <string.h>
#include <vector>
#include <algorithm>
#include "fpng.h"

// Simple little-endian 32-bit read
static inline uint32_t rd32le(const uint8_t* p) {
  uint32_t v; memcpy(&v, p, sizeof(v)); return v;
}

extern "C" int LLVMFuzzerInitialize(int*, char***) {
  fpng::fpng_init();
  return 0;
}

extern "C" int LLVMFuzzerTestOneInput(const uint8_t* data, size_t size) {
  if (!data || size < 9) return 0;

  // Input format (fuzzer generated): [w:4][h:4][channels:1][pixels...]
  // Set limits to prevent OOM: max 4096x4096, pixel data max 64MB
  const uint8_t* p = data;
  uint32_t w = rd32le(p); p += 4;
  uint32_t h = rd32le(p); p += 4;
  uint32_t chans = (*p & 1) ? 3u : 4u; p += 1;

  // Normalize/clamp to avoid extreme values
  w = std::max<uint32_t>(1, std::min<uint32_t>(w, 4096));
  h = std::max<uint32_t>(1, std::min<uint32_t>(h, 4096));

  // Required number of pixel bytes
  const uint64_t need = (uint64_t)w * h * chans;
  if (need > (1ull<<26)) return 0; // Discard if >64MB

  // If input is insufficient, fill by repeating/looping (ensure encoding path is taken without excessive allocation)
  std::vector<uint8_t> pixels;
  pixels.resize((size_t)need);
  for (size_t i = 0; i < (size_t)need; ++i) {
    pixels[i] = data[ (9 + i) % size ];
  }

  // Encode to memory
  std::vector<uint8_t> png_buf;
  (void)fpng::fpng_encode_image_to_memory(
      pixels.data(), w, h, chans, png_buf, 0 /* flags: single-channel fast path */);

  // (Optional) Round-trip decoding to cover the specific decoding path for "PNGs generated by fpng"
  if (!png_buf.empty()) {
    std::vector<uint8_t> dec;
    uint32_t dw=0, dh=0, dch=0;
    (void)fpng::fpng_decode_memory(
        png_buf.data(), (uint32_t)png_buf.size(), dec, dw, dh, dch, chans);
  }
  return 0;
}
